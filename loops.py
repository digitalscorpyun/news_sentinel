# For loop: Counting from 1 to 5
for i in range(1, 6):  # Start at 1, go up to but not including 6
    print(f"This is loop iteration {i}.")

# While loop: Countdown from 5 to 1
count = 5  # Start at 5
while count > 0:  # Loop until count is no longer greater than 0
    print(f"Counting down: {count}")
    count -= 1  # Decrease count by 1 each time
print("Blast off!")
