# Desc: This program demonstrates how to work with lists in Python
bicycles = ['trek', 'cannondale', 'redline', 'specialized']
print(bicycles)
# Accessing the first element in the list
print(bicycles[0])
# Accessing an element in the list and using the title() method to capitalize the first letter
print(bicycles[0].title())
# Accessing multiple elements in the list
print(bicycles[1])
print(bicycles[3])
# Accessing the last element in the list
print(bicycles[-1])